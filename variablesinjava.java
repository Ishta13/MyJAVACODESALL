public class variablesinjava {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a);
        System.out.println(b);
        System.out.println("a");
        String qwerty = "james bond";
        System.out.println(qwerty);
        a=b;                       // b to a value changes (a value depends on b)
        System.out.println(a);    
        a=8;
        System.out.println(a);
        b=a;                     // a to b value changes  (b valye depends on a)
        System.out.println(b);  //left to right value changes 
        int c=12, d=9;
        System.out.println(c);
        System.out.println(d);
        


    }
}
